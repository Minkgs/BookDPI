    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        RepositorisBook rp = new RepositorisBook();
        if (txtUsername.getText().isEmpty() && txtPassword.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "You have not entered your username or password");
        } else if (txtUsername.getText().equals("sa") && txtPassword.getText().equals("sa")) {
            viewBook book = new viewBook();
            book.setVisible(true);
            book.loadTableSa(rp.welcomSa());
        } else if (txtUsername.getText().equals("an") && txtPassword.getText().contains("an")) {
            viewBook book = new viewBook();
            book.setVisible(true);
            book.loadTableAn(rp.welcomAn());
        } else if (txtUsername.getText().equals("nam") && txtPassword.getText().contains("nam")) {
            viewBook book = new viewBook();
            book.setVisible(true);
            book.loadTableNam(rp.welcomNam());
        }else{
            lbThongbao.setText("Account or password is incorrect!");
        }
    }//GEN-LAST:event_btnLoginActionPerformed

